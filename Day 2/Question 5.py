print('Questions 5. Strings and explore default methods.')
str = 'this is a string'
# 1 capitalize() - to convert character to upper-case
print(str.capitalize())
# 2 find() - Searches the string for a specified value and returns the position of where it was found
print(str.find('is'))
# 3 lower() - Converts a string into lower case
print(str.lower())
# 4 replace() - Returns a string where a specified value is replaced with a specified value
print(str.replace('is', 'a'))
# 5 title() - Converts the first character of each word to upper case
print(str.title())